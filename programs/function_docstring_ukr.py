def print_max(x, y):
    '''Виводить більше із двох чисел.

    Обидва значення мають бути цілими числами.'''
    # конвертувати в цілі числа, якщо можливо
    x = int(x)
    y = int(y)

    if x > y:
        print(x, 'найбільше')
    else:
        print(y, 'найбільше')

print_max(3, 5)
