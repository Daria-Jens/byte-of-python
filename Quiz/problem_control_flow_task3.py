# Задача з розділу "Потік керування", завдання 3
дохід = input("Будь ласка, введіть свій щомісячний (чистий) дохід в євро")
дохід = int(дохід)
if дохід < 1000:
    print("Ти небагато заробляєш...")
if дохід < 2000:
    print("Могло б бути краще...")
elif дохід < 3000:
    print("Добре")
if дохід < 4000:
    print("Дуже добре")
if дохід < 5000:
    print("Чудово")
else:
    print("Насправді??")
