# Розв’язання для розділу «Потік керування», завдання 1, варіант E
спроба = 1
while input("Будь ласка, введіть пароль: >>>") != "SeCrEt":
    print("Неправильно")
    спроба += 1
    if спроба > 3:
        print("Ви зробили 3 невдалі спроби")
        break
else:
    print("Правильно")
print("Бувай!")
